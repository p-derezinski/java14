package pl.sdacademy.java14poz.grudzien0809.zadanie_8_SortowaniePrzezZliczanie;

public class ClassWithPrintMethod {

    public static void printTable(int[] tab) {
        for (int i = 0; i < tab.length; i++) {
            System.out.print(tab[i] + " ");
        }
        System.out.println();
    }

}
